function convert() {
    const input = document.querySelector("#input").value;
    const selectedFont = fonts[document.querySelector("#font").value]
    const validChars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789".slice(0, selectedFont.length);
    let output = ""
    for (let i of input) {
        if (validChars.includes(i)) {
            output += String.fromCodePoint(selectedFont[validChars.indexOf(i)]);
        } else {
            output += i;
        }
    }
    document.querySelector("#output").innerHTML = output;
}